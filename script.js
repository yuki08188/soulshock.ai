/* ══════════════════════════════════════════════════════
   LANGUAGE ENGINE
   ══════════════════════════════════════════════════════ */
let currentLang = localStorage.getItem('ss-lang') || 'zh';

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('ss-lang', lang);
  const t = T[lang];
  if (!t) return;

  // Direction
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);

  // Plain text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Inner HTML nodes (contain <em>, <br/>, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Agent chips (8 cards)
  for (let i = 1; i <= 8; i++) {
    const chipsKey = `a${i}-chips`;
    const chips = t[chipsKey];
    const container = document.querySelector(`[data-chips="a${i}"]`);
    if (chips && container) {
      container.innerHTML = chips
        .map(c => `<span class="app-agent-chip">${c}</span>`)
        .join('');
    }
  }

  // Search placeholder
  const searchInput = document.getElementById('searchInput');
  if (searchInput && t['nav-search-ph']) {
    searchInput.placeholder = t['nav-search-ph'];
  }

  // Update lang button label
  const langLabel = document.getElementById('langLabel');
  const langNames = { zh:'中文', en:'English', fr:'Français', ar:'العربية', es:'Español' };
  if (langLabel) langLabel.textContent = langNames[lang] || lang;

  // Mark active option
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
  });
}

